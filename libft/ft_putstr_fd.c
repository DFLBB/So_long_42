#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	c;

	c = 0;
	if (s)
	{
		while (c < ft_strlen(s))
		{
			write(fd, &s[c], sizeof(s[c]));
			c++;
		}
	}
	else
		c = 0;
}
